-- H2 Schema (reference): FUNCTION, ADDRESS, PERSON
-- The live schema is generated from the JPA entities at startup
-- (spring.jpa.hibernate.ddl-auto=create-drop). This file is kept for documentation.
-- Run in this order due to foreign key dependencies.

CREATE TABLE FUNCTION (
    ID          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    LABEL       VARCHAR(100)    NOT NULL,
    DESCRIPTION VARCHAR(500),
    CREATED_AT  TIMESTAMP       DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT  TIMESTAMP       DEFAULT CURRENT_TIMESTAMP NOT NULL
);

CREATE TABLE ADDRESS (
    ID          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    STREET      VARCHAR(200)    NOT NULL,
    CITY        VARCHAR(100)    NOT NULL,
    ZIP_CODE    VARCHAR(20)     NOT NULL,
    COUNTRY     VARCHAR(100)    DEFAULT 'France' NOT NULL,
    CREATED_AT  TIMESTAMP       DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT  TIMESTAMP       DEFAULT CURRENT_TIMESTAMP NOT NULL
);

CREATE TABLE PERSON (
    ID           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FIRST_NAME   VARCHAR(100)   NOT NULL,
    LAST_NAME    VARCHAR(100)   NOT NULL,
    EMAIL        VARCHAR(255)   NOT NULL,
    PHONE        VARCHAR(30),
    ADDRESS_ID   BIGINT         NOT NULL,
    FUNCTION_ID  BIGINT         NOT NULL,
    CREATED_AT   TIMESTAMP      DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT   TIMESTAMP      DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT UQ_PERSON_EMAIL       UNIQUE (EMAIL),
    CONSTRAINT FK_PERSON_ADDRESS     FOREIGN KEY (ADDRESS_ID)  REFERENCES ADDRESS(ID),
    CONSTRAINT FK_PERSON_FUNCTION    FOREIGN KEY (FUNCTION_ID) REFERENCES FUNCTION(ID)
);

CREATE INDEX IDX_PERSON_ADDRESS  ON PERSON(ADDRESS_ID);
CREATE INDEX IDX_PERSON_FUNCTION ON PERSON(FUNCTION_ID);
